async function seed() {
  console.log("Заңдарды Firestore-ға жүктеу басталуда...");
  const lawsRef = collection(db, 'laws');
  
  for (const law of lawsData) {
    // Unique ID-ні кодекс мен бап нөмірінен жасаймыз
    const id = `${law.code}_${law.article}`.replace(/[^a-zA-Z0-9]/g, '_');
    await setDoc(doc(lawsRef, id), law);
    console.log(`Жүктелді: ${law.title}`);
  }
  
  console.log("Барлығы сәтті аяқталды! ✅");
  process.exit(0);
}

seed().catch(err => {
  console.error("Қате:", err);
  process.exit(1);
});
