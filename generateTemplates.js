import * as fs from 'fs';
import { Document, Packer, Paragraph, TextRun, AlignmentType, HeadingLevel } from 'docx';

if (!fs.existsSync('./public/templates')) {
  fs.mkdirSync('./public/templates', { recursive: true });
}

async function generateDoc(filename, title, contentLines) {
  const children = [
    new Paragraph({
      text: title,
      heading: HeadingLevel.HEADING_1,
      alignment: AlignmentType.CENTER,
      spacing: { after: 400 },
    })
  ];

  for (const line of contentLines) {
    if (line.trim() === '') {
      children.push(new Paragraph({ text: '', spacing: { after: 200 } }));
      continue;
    }
    
    // Check if it's right-aligned (for header structures in Kazakh applications)
    if (line.startsWith('RIGHT:')) {
      children.push(new Paragraph({
        children: [new TextRun({ text: line.replace('RIGHT:', '').trim(), size: 24 })],
        alignment: AlignmentType.RIGHT,
      }));
    } else {
      children.push(new Paragraph({
        children: [new TextRun({ text: line, size: 24 })],
        alignment: AlignmentType.JUSTIFIED,
        spacing: { after: 200 },
      }));
    }
  }

  const doc = new Document({
    sections: [{
      properties: {},
      children: children,
    }],
  });

  const buffer = await Packer.toBuffer(doc);
  fs.writeFileSync(`./public/templates/${filename}`, buffer);
  console.log(`Generated ${filename}`);
}

